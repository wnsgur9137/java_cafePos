    private void btnIDCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIDCheckActionPerformed
        // 회원가입 아이디 중복 체크 버튼
        
        if(txtJoinID.getText().length() < 1) {
            System.out.println("아이디를 입력해주십시오.");
            JOptionPane.showMessageDialog(null, "아이디를 입력해 주십시오.", "중복 확인 실패", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String userID = txtJoinID.getText();
        
        UserDAO userDAO = new UserDAO();
        int result = userDAO.idCheck(userID); // 아이디 중복 체크
        if(result != 1) { // 아이디가 중복일 경우(동일한 아이디가 DB에 이미 있는 경우)
            System.out.println("중복");
            JOptionPane.showMessageDialog(null, "아이디가 중복입니다.", "중복 확인 실패", JOptionPane.ERROR_MESSAGE);
            idCheck = 0; // idCheck 전역변수 0 (아이디 중복)
        } else { // 아이디 중복이 아닌 경우
            System.out.println("아이디 사용 가능");
            JOptionPane.showMessageDialog(null, "사용가능한 아이디입니다.", "중복 확인 성공", JOptionPane.INFORMATION_MESSAGE);
            idCheck = 1; // idCheck  전역변수 1 (사용 가능)
        }
    }//GEN-LAST:event_btnIDCheckActionPerformed

