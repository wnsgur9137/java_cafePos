    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // 로그인 버튼
        
        // ID, PWD 텍스트 필드에 값을 넣지 않은 경우
        if(txtID.getText().length() < 1 ||
           txtPWD.getText().length() < 1) {
           System.out.println("아이디, 비밀번호를 입력해 주십시오.");
           JOptionPane.showMessageDialog(null, "아이디, 비밀번호를 입력해 주십시오.", "로그인 실패", JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        String userID = txtID.getText();
        String userPWD = txtPWD.getText();
        
        UserDAO userDAO = new UserDAO();
        int result = userDAO.login(userID, userPWD);
        if(result != 1) { // 로그인 실패일 경우
            System.out.println("로그인 실패");
            JOptionPane.showMessageDialog(null, "정보가 일치하지 않습니다.", "로그인 실패", JOptionPane.ERROR_MESSAGE);
        } else { // 로그인 성공일 경우
            JOptionPane.showMessageDialog(null, "환영합니다.", "로그인 성공", JOptionPane.INFORMATION_MESSAGE);
            System.out.println("로그인 성공");
            loginResult = 1;
            dispose();
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed
    
