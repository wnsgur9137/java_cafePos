    // 회원가입 -------------------------------------
    
    // 유저 키는 오라클 스퀸스 사용
    // CREATE SEQUENCE users_seq START WITH 1 INCREMENT BY 1 MAXVALUE 200 NOCYCLE NOCACHE;
    
    // -1일경우 중복 확인 안함, 0일경우 중복, 1일경우 사용 가능한 아이디
    int idCheck = -1;
    
    int nullCheck() { // 회원가입 프레임의 텍스트 필드 값이 입력되었는지 확인
        int result = 1;
        if (txtJoinName.getText().length() < 1 ||
            txtJoinID.getText().length() < 1 ||
            txtJoinPWD.getText().length() < 1 ||
            txtJoinPWDCheck.getText().length() < 1 ||
            txtJoinEmail.getText().length() < 1) {
   
            result = -1;
        }
        return result;
    }
    
    private void btnJoinActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJoinActionActionPerformed
        // 회원가입 버튼
        
        if(idCheck == -1) { // 중복체크를 하지 않은 경우
            System.out.println("중복체크 해 주십시오.");
            JOptionPane.showMessageDialog(null, "중복 확인 해 주십시오..", "회원가입 실패", JOptionPane.ERROR_MESSAGE);
        } else if(idCheck == 0) { // 아이디가 중복인 경우
            System.out.println("아이디 중복입니다.");
            JOptionPane.showMessageDialog(null, "아이디 중복입니다.", "회원가입 실패", JOptionPane.ERROR_MESSAGE);
        }
        
        int emptyCheck = nullCheck(); // 회원가입에 필요한 정보들을 모두 입력했는지 확인
        if(emptyCheck != 1) { // 정보를 입력하지 않은 경우
            System.out.println("빈칸을 채워주십시오.");
            JOptionPane.showMessageDialog(null, "빈칸을 채워주십시오.", "회원가입 실패", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String userName = txtJoinName.getText();
        String userID = txtJoinID.getText();
        String userPWD = txtJoinPWD.getText();
        String userPWDCheck = txtJoinPWDCheck.getText();
        String userEmail = txtJoinEmail.getText();
        
        // 유저 생성 날짜/시간
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        String userCreated = dateFormat.format(date);
        
        
        if(!userPWD.equals(userPWDCheck)) { // 비밀번호와 비밀번호 확인 텍스트 필드가 서로 일치하지 않은 경우
            System.out.println("비밀번호가 일치하지 않습니다.");
            JOptionPane.showMessageDialog(null, "비밀번호가 일치하지 않습니다.", "회원가입 실패", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(userEmail.length() < 1 || !userEmail.contains("@") || !userEmail.contains(".")) { // 입력한 이메일이 형식에 맞지 않을 경우
            System.out.println("이메일 형식에 맞게 입력해주십시오.");
            JOptionPane.showMessageDialog(null, "이메일을 형식에 맞게 입력해 주십시오..", "회원가입 실패", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        
        User user = new User(null, userName, userID, userPWD, userEmail, userCreated);
        UserDAO userDAO = new UserDAO();
        
        int result = userDAO.join(user);
        if(result != 1) { // 회원가입 실패인 경우
            System.out.println("회원가입 실패");
            JOptionPane.showMessageDialog(null, "회원가입 실패.", "회원가입 실패", JOptionPane.ERROR_MESSAGE);
        } else { // 회원가입 성공인 경우
            System.out.println("회원가입 성공");
            JOptionPane.showMessageDialog(null, "회원가입에 성공하였습니다.", "회원가입 성공", JOptionPane.INFORMATION_MESSAGE);
            txtClear();
            joinForm.dispose(); // 회원가입 창 종료
        }
        
    }//GEN-LAST:event_btnJoinActionActionPerformed

